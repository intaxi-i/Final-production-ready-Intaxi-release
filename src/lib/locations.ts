export type RegionKey = keyof typeof UZBEKISTAN_REGIONS;

export function getRegionOptions(lang: string) {
  return Object.entries(UZBEKISTAN_REGIONS).map(([key, value]) => ({
    key,
    label: (value as any)[lang] || value.ru,
  }));
}

export function getLocalityOptions(regionKey: string) {
  const region = (UZBEKISTAN_REGIONS as Record<string, any>)[regionKey];
  return region ? region.localities as string[] : [];
}

export function formatUzLocation(regionKey: string, locality: string, lang = "ru") {
  const region = (UZBEKISTAN_REGIONS as Record<string, any>)[regionKey];
  if (!region) return locality || "";
  const regionLabel = region[lang] || region.ru;
  if (!locality) return regionLabel;
  return `${regionLabel} / ${locality}`;
}

export function guessUzRegionFromCity(city: string) {
  if (!city) return "";
  const normalized = city.toLowerCase();
  for (const [key, region] of Object.entries(UZBEKISTAN_REGIONS)) {
    if (String((region as any).ru || "").toLowerCase() === normalized) return key;
    if (String((region as any).uz || "").toLowerCase() === normalized) return key;
    if (((region as any).localities || []).some((item: string) => item.toLowerCase() === normalized)) {
      return key;
    }
  }
  return "";
}